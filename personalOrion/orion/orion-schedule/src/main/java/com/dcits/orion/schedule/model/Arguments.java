package com.dcits.orion.schedule.model;

import java.util.List;

import com.dcits.galaxy.base.bean.ArgumentBean;
import com.thoughtworks.xstream.annotations.XStreamAlias;
import com.thoughtworks.xstream.annotations.XStreamImplicit;

/**
 * @description
 * @version V1.0
 * @author Tim
 * @update 2014年10月30日 下午1:24:42
 */

@XStreamAlias("arguments")
public class Arguments {

	@XStreamImplicit(itemFieldName = "arg")
	List<ArgumentBean> arg;

	/**
	 * @return arg : return the property arg.
	 */
	public List<ArgumentBean> getArg() {
		return arg;
	}

	/**
	 * @param arg
	 *            : set the property arg.
	 */
	public void setArg(List<ArgumentBean> arg) {
		this.arg = arg;
	}
}
