package com.dcits.orion.schedule.model;

public class Partition {

	public Partition(String extractors, String loaders) {
		this.extractors = extractors;
		this.loaders = loaders;
	}

	private String extractors;

	private String loaders;

	/**
	 * @return extractors : return the property extractors.
	 */
	public String getExtractors() {
		return extractors;
	}

	/**
	 * @param extractors
	 *            : set the property extractors.
	 */
	public void setExtractors(String extractors) {
		this.extractors = extractors;
	}

	/**
	 * @return loaders : return the property loaders.
	 */
	public String getLoaders() {
		return loaders;
	}

	/**
	 * @param loaders
	 *            : set the property loaders.
	 */
	public void setLoaders(String loaders) {
		this.loaders = loaders;
	}

	public String toString() {
		return "extractors:" + extractors + ",loaders:" + loaders;
	}
}
