package com.dcits.orion.stria.entity;

import com.dcits.galaxy.base.bean.AbstractBean;

import java.util.List;

/**
 * Created by Tim on 2015/5/6.
 */
public class Mapper extends AbstractBean {

    private static final long serialVersionUID = -8408526088262129014L;

    private String name; // 业务bean的别名
    private String clazz; // 业务bean的class实现类
    private String condition; // 映射条件
    private List<Item> item;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getClazz() {
        return clazz;
    }

    public void setClazz(String clazz) {
        this.clazz = clazz;
    }

    public String getCondition() {
        return condition;
    }

    public void setCondition(String condition) {
        this.condition = condition;
    }

    public List<Item> getItem() {
        return item;
    }

    public void setItem(List<Item> item) {
        this.item = item;
    }
}
