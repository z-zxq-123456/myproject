package com.dcits.orion.stria.exception;

import com.dcits.galaxy.base.exception.GalaxyException;

/**
 * @description
 * @version V1.0
 * @author Tim
 * @update 2014年12月18日 下午4:11:14
 */

public class StriaException extends GalaxyException {

	/**
	 * @fields serialVersionUID
	 */

	private static final long serialVersionUID = 3985422809785623924L;

	/**
	 * Creates a new GalaxyException.
	 */
	public StriaException() {
		super();
	}

	/**
	 * Constructs a new GalaxyException.
	 *
	 * @param message
	 *            the reason for the exception
	 */
	public StriaException(String message) {
		super(message);
	}

	/**
	 * Constructs a new GalaxyException.
	 *
	 * @param cause
	 *            the underlying Throwable that caused this exception to be
	 *            thrown.
	 */
	public StriaException(Throwable cause) {
		super(cause);
	}

	/**
	 * Constructs a new GalaxyException.
	 *
	 * @param message
	 *            the reason for the exception
	 * @param cause
	 *            the underlying Throwable that caused this exception to be
	 *            thrown.
	 */
	public StriaException(String message, Throwable cause) {
		super(message, cause);
	}

}
