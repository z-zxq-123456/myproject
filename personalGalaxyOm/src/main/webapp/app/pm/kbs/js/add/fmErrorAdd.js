
$(document).ready(function() {
	getPkList({
		url: contextPath + "/baseCommon/pklistBase?tableName=FM_COMPANY&tableCol=COMPANY,COMPANY_NAME",
		id: "company",
		async: false
	});

	$("#fmErrorAdd").Validform({
		tiptype:2,
		callback:function(form){
			fmErrorAdd();
			return false;
		}
	});

	$(".select2").select2();
});

function fmErrorAdd(){
	var paraJson,generalFieldsJson,keyFieldsJson;
	paraJson = {};
	generalFieldsJson={};
	keyFieldsJson={};
	var url = contextPath+"/baseCommon/updateAndInsertForSave";
	paraJson.tableName="FM_ERROR";
		keyFieldsJson.ERROR_NO=$("#errorNo").val();
		keyFieldsJson.ERROR_LANG=$("#errorLang").val();
		generalFieldsJson.SEVERITY_LEVEL=$("#severityLevel").val();
		generalFieldsJson.ERROR_MSG=$("#errorMsg").val();
		generalFieldsJson.COMPANY=$("#company").val();
	paraJson.key = keyFieldsJson;
	paraJson.general=generalFieldsJson;
	paraJson.reqNum = parent.parent.$(".breadcrumb").data("reqNum");
	var params = {
		paraJson:JSON.stringify(paraJson)
	};
	sendPostRequest(url,params, callback_fmErrorAdd,"json");
}

function callback_fmErrorAdd(json){
	if (json.success) {
     parent.showMsgDuringTime("添加成功");
	} else if (json.errorMsg) {
		showMsg(json.errorMsg, 'errorMsg');
	}
}

function fmErrorAddCancel(){
	var index = parent.layer.getFrameIndex(window.name);  //获取窗口索引
    parent.layer.close(index);  //关闭窗口
}


